<div class="instructions">
	<h2>Good example of contour map</h2>
	<div class="example" />

	<h2>Instructions</h2>

	<div class="section">Drawing</div>
	<div class="instruction">
		<div class="title">Curves should have sufficient margin</div>
		<div class="description">
			The curves should have sufficient margin between them, overlapping or close lines are hard to
			interpret
		</div>
	</div>
	<div class="instruction">
		<div class="title">Drawing with dark pen</div>
		<div class="description">
			To recognise your level curves, we prefer a dark and thick marker. Using a lighter pen will
			likely not work
		</div>
	</div>
	<div class="instruction">
		<div class="title">Close your curves</div>
		<div class="description">
			To avoid false readings, close your curves, dont leave any open-ended lines
		</div>
	</div>

	<div class="section">Scanning</div>
	<div class="instruction">
		<div class="title">Four corners visible</div>
		<div class="description">
			To scan the level curves, we need to have the four corners visible
		</div>
	</div>
	<div class="instruction">
		<div class="title">Drawing should not be too far</div>
		<div class="description">
			Scanning works best from a distance of 0.5 meters from the piece of paper. For best the best
			result try to scan the paper as close to parallel as possible. This is easiest if you put your
			drawing on a table instead of holding it.
		</div>
	</div>
	<div class="instruction">
		<div class="title">Proper lighting</div>
		<div class="description">
			Level curves are hard to distinguish from the background in a low light setting. Try to use natural
			daylight or a propper ceiling light for the best results. Be careful of sharp or dark shadows on
			the drawing, as they might get interpreted as part of your drawing.
		</div>
	</div>
</div>

